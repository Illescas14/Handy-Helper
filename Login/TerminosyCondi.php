<div class="section">
    <p>Contrato de Términos y Condiciones para Usuarios de Handy Helper.</p>
</div>

<div class="section">
    <h2>1. Información Relevante</h2>
    <p>El Usuario reconoce que, para contratar y/o utilizar los servicios esporádicos ofrecidos en la plataforma Handy Helper, es imprescindible que lea y acepte estos Términos y Condiciones. El uso de la plataforma implica que el Usuario ha leído, entendido y aceptado todos los términos aquí establecidos.</p>
</div>

<div class="section">
    <h2>2. Información que es Recogida</h2>
    <p>Handy Helper recopila información personal del Usuario, que incluye, pero no se limita a:</p>
    <ul>
        <li>Nombre completo</li>
        <li>Número de identificación oficial (INE)</li>
        <li>Dirección de correo electrónico</li>
        <li>Número de teléfono</li>
        <li>Información demográfica (edad, género, ubicación)</li>
    </ul>
    <p>Esta información es fundamental para procesar la contratación de servicios esporádicos y mejorar la experiencia del Usuario en la plataforma.</p>
</div>

<div class="section">
    <h2>3. Uso de la Información Recogida</h2>
    <p>La información recopilada se utilizará para:</p>
    <ul>
        <li>Procesar y gestionar la contratación de servicios esporádicos.</li>
        <li>Mantener un registro de usuarios y su historial de contrataciones.</li>
        <li>Enviar comunicaciones periódicas sobre ofertas, promociones y servicios relevantes. El Usuario puede cancelar la suscripción a estos correos electrónicos en cualquier momento.</li>
    </ul>
    <p>Handy Helper se compromete a proteger la información personal del Usuario mediante sistemas avanzados y actualizados para prevenir accesos no autorizados.</p>
</div>

<div class="section">
    <h2>4. Cookies</h2>
    <p>El uso de cookies en la plataforma permite identificar las páginas visitadas y la frecuencia de acceso, mejorando la experiencia del Usuario. Las cookies ayudan a personalizar el contenido y los anuncios. El Usuario puede rechazar el uso de cookies a través de la configuración de su navegador, aunque esto podría limitar el acceso a ciertos servicios de la plataforma.</p>
</div>

<div class="section">
    <h2>5. Enlaces a Terceros</h2>
    <p>La plataforma Handy Helper puede contener enlaces a sitios de terceros. Handy Helper no es responsable de las políticas de privacidad o el contenido de dichos sitios. Se recomienda al Usuario leer los Términos y Condiciones y las políticas de privacidad de cada sitio antes de proporcionar cualquier información personal.</p>
</div>

<div class="section">
    <h2>6. Control de su Información Personal</h2>
    <p>El Usuario tiene el derecho de restringir la recopilación o el uso de su información personal en cualquier momento. Handy Helper no venderá ni distribuirá la información personal del Usuario sin su consentimiento, salvo que sea requerido por orden judicial.</p>
</div>

<div class="section">
    <h2>7. Deslinde de Responsabilidad</h2>
    <p>El Usuario reconoce que es su responsabilidad supervisar y atender a los contratistas y empleados con los que establezca contacto a través de la plataforma. Cualquier accidente, extravío o incidente que ocurra durante la prestación de servicios esporádicos será de su exclusiva responsabilidad. Handy Helper y sus afiliados no se hacen responsables de tales eventos y el Usuario deslinda a Handy Helper de toda responsabilidad.</p>
</div>

<div class="section">
    <h2>8. Política de Reembolsos</h2>
    <p>Los Clientes pueden ser elegibles para un reembolso bajo las siguientes condiciones:</p>
    <ul>
        <li>No cumplimiento del servicio: Si el Proveedor de Servicios no completa el servicio contratado según los términos acordados.</li>
        <li>Servicio insatisfactorio: Si el servicio prestado no cumple con los estándares razonables esperados.</li>
        <li>Cancelación por el Proveedor de Servicios: Si el Proveedor cancela el servicio antes de completarlo.</li>
        <li>Errores de facturación: En caso de errores en la facturación o cobros duplicados.</li>
    </ul>
    <p>Para solicitar un reembolso, el Usuario deberá enviar una solicitud a través del sistema de soporte de la plataforma dentro de los 7 días posteriores a la fecha del servicio.</p>
</div>

<div class="section">
    <h2>Procedimiento para Solicitar el Reembolso</h2>
    <p>Para solicitar un reembolso, el Cliente debe:</p>
    <ol>
        <li>Presentar una Solicitud de Reembolso a través del sistema de soporte en el plazo establecido de 7 días posteriores a la fecha del servicio.</li>
        <li>Proporcionar detalles sobre el servicio y la razón de la solicitud, incluyendo evidencia relevante.</li>
        <li>Nuestro equipo revisará la solicitud y se comunicará con ambas partes si es necesario.</li>
    </ol>
</div>

<div class="section">
    <h2>Resolución de Solicitudes</h2>
    <p>El equipo de soporte evaluará las solicitudes considerando la comunicación y los registros entre el Cliente y el Proveedor de Servicios, así como la evidencia proporcionada.</p>
</div>

<div class="section">
    <h2>Decisiones sobre Reembolsos</h2>
    <p>Las decisiones se comunicarán dentro de un plazo determinado y pueden incluir:</p>
    <ul>
        <li>Aprobación del Reembolso: El Cliente recibirá el reembolso total o parcial.</li>
        <li>Rechazo del Reembolso: Se proporcionará una explicación detallada del motivo del rechazo.</li>
    </ul>
</div>

<div class="section">
    <h2>Excepciones y Consideraciones Especiales</h2>
    <p>No se otorgarán reembolsos por servicios completados de manera satisfactoria. Se puede otorgar un reembolso parcial si el servicio fue parcialmente satisfactorio. Las cancelaciones del Cliente estarán sujetas a las políticas del Proveedor, que pueden incluir tarifas de cancelación.</p>
</div>

<div class="section">
    <h2>Comprobación Antifraude</h2>
    <p>La contratación del cliente puede ser aplazada para la comprobación antifraude. Esta puede ser suspendida por más tiempo para evitar transacciones fraudulentas.</p>
</div>

<div class="section">
    <h2>Modificaciones a la Política de Reembolsos</h2>
    <p>Handy Helper se reserva el derecho de modificar esta política en cualquier momento, publicando las actualizaciones en nuestra plataforma.</p>
</div>

